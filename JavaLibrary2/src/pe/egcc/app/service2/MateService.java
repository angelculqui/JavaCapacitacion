
package pe.egcc.app.service2;

public class MateService2 {
    public int sumar(int n1, int n2){
        /**
         * Calcuala la suma de dos numeros
         * @param n1 Número 1
         * @param n2 Número 2
         * @Re
        */
        return (n1+n2);
    }
     public int restar(int n1, int n2){
        /**
         * Calcuala la suma de dos numeros
         * @param n1 Número 1
         * @param n2 Número 2
         * @Re
        */
        return (n1-n2);
    }
     
}
